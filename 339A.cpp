int T = 1;

vector<string> split (const string &s, char delim) {
    vector<string> result;
    stringstream ss (s);
    string item;

    while (getline (ss, item, delim)) {
        result.push_back (item);
    }

    return result;
}

void sol() {
	string s;
	cin >> s;
	vt<string> v = split(s,'+');
	vt<int> res;
	for(int i=0; i<sz(v); ++i) {
		res.pb(stoi(v[i]));
	}
	sort(all(res));
	for(int i=0; i<sz(res); ++i) {
		cout << res[i];
		if(i != sz(res)-1) cout << '+';
	}
	cout << en;

	return;
}

int main() {
	ios_base::sync_with_stdio(0); cin.tie(0);

	while(T--) {
		sol();
	}

	return 0;
}